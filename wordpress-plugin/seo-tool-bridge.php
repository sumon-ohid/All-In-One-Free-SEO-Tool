function stb_render_revisions(): void
{
    $revisions = get_option(STB_OPTION_REVISIONS, []);
    if (empty($revisions)) {
        echo '<p>No changes applied yet.</p>';
        return;
    }
    echo '<table class="widefat striped" style="max-width: 900px;">';
    echo '<thead><tr><th>When</th><th>Field</th><th>Object</th><th>Old → New</th></tr></thead><tbody>';
    foreach (array_slice(array_reverse($revisions), 0, 30) as $r) {
        printf(
            '<tr><td>%s</td><td>%s</td><td>%s</td><td><code>%s</code> → <code>%s</code></td></tr>',
            esc_html(date('Y-m-d H:i', $r['ts'])),
            esc_html($r['field']),
            esc_html($r['object']),
            esc_html(mb_substr((string)($r['old'] ?? ''), 0, 80)),
            esc_html(mb_substr((string)($r['new'] ?? ''), 0, 80))
        );
    }
    echo '</tbody></table>';
}

