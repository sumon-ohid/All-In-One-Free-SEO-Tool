add_action('rest_api_init', function () {
    register_rest_route(STB_REST_NAMESPACE, '/ping', [
        'methods'  => 'GET',
        'callback' => 'stb_rest_ping',
        'permission_callback' => 'stb_check_key',
    ]);

    // Single route, two methods. Calling register_rest_route twice with
    // the same path made WP's REST router behavior depend on call order
    // — the second call would shadow the first, breaking GET. Passing
    // an array of method configs is the documented WP way.
    register_rest_route(STB_REST_NAMESPACE, '/post/(?P<id>\d+)/seo', [
        [
            'methods'  => 'GET',
            'callback' => 'stb_rest_get_post_seo',
            'permission_callback' => 'stb_check_key',
            'args' => ['id' => ['validate_callback' => 'is_numeric']],
        ],
        [
            'methods'  => 'POST',
            'callback' => 'stb_rest_update_post_seo',
            'permission_callback' => 'stb_check_key',
            'args' => ['id' => ['validate_callback' => 'is_numeric']],
        ],
    ]);

    register_rest_route(STB_REST_NAMESPACE, '/attachment/(?P<id>\d+)/alt', [
        'methods'  => 'POST',
        'callback' => 'stb_rest_update_alt',
        'permission_callback' => 'stb_check_key',
        'args' => ['id' => ['validate_callback' => 'is_numeric']],
    ]);

    register_rest_route(STB_REST_NAMESPACE, '/post/(?P<id>\d+)/schema', [
        'methods'  => 'POST',
        'callback' => 'stb_rest_set_schema',
        'permission_callback' => 'stb_check_key',
        'args' => ['id' => ['validate_callback' => 'is_numeric']],
    ]);

    register_rest_route(STB_REST_NAMESPACE, '/posts/list', [
        'methods'  => 'GET',
        'callback' => 'stb_rest_list_posts',
        'permission_callback' => 'stb_check_key',
    ]);

    // Resolve a public URL to a post ID. Used by the SEO Tool's
    // one-click fix flow so users only need a URL, not the post ID.
    register_rest_route(STB_REST_NAMESPACE, '/find', [
        'methods'  => 'GET',
        'callback' => 'stb_rest_find_by_url',
        'permission_callback' => 'stb_check_key',
        'args' => ['url' => ['validate_callback' => fn($v) => is_string($v) && strlen($v) > 3]],
    ]);

    register_rest_route(STB_REST_NAMESPACE, '/revisions', [
        'methods'  => 'GET',
        'callback' => 'stb_rest_revisions',
        'permission_callback' => 'stb_check_key',
    ]);

    register_rest_route(STB_REST_NAMESPACE, '/undo/(?P<rev_id>\d+)', [
        'methods'  => 'POST',
        'callback' => 'stb_rest_undo',
        'permission_callback' => 'stb_check_key',
        'args' => ['rev_id' => ['validate_callback' => 'is_numeric']],
    ]);

    // v0.2.0+ — create a post. Used by daily automation when a blog_draft
    // queue item is approved + published. Status defaults to "draft" so
    // the WP editor gets a final pass before going live; schedules with
    // auto_publish=true pass "publish" to skip that.
    register_rest_route(STB_REST_NAMESPACE, '/posts', [
        'methods'  => 'POST',
        'callback' => 'stb_rest_create_post',
        'permission_callback' => 'stb_check_key',
    ]);
});

function stb_check_key(WP_REST_Request $req): bool
{
    $auth = $req->get_header('authorization') ?? '';
    $expected = get_option(STB_OPTION_KEY);
    if (!$expected) {
        return false;
    }
    $provided = preg_replace('/^Bearer\s+/i', '', $auth);
    return hash_equals($expected, trim($provided));
}

function stb_rest_ping(): WP_REST_Response
{
    return new WP_REST_Response([
        'ok' => true,
        'plugin_version' => STB_VERSION,
        'wp_version' => get_bloginfo('version'),
        'site_url' => home_url(),
        'capabilities' => [
            'meta_titles' => true,
            'meta_descriptions' => true,
            'image_alt' => true,
            'schema' => true,
            'redirects' => true,
        ],
    ]);
}

function stb_get_meta_description(int $post_id): string
{
    // Try the SEO plugins in priority order
    $candidates = [
        '_yoast_wpseo_metadesc',          // Yoast
        'rank_math_description',           // Rank Math
        '_aioseop_description',            // All in One SEO (legacy)
        '_aioseo_description',             // All in One SEO (current)
    ];
    foreach ($candidates as $key) {
        $val = get_post_meta($post_id, $key, true);
        if (!empty($val)) {
            return (string)$val;
        }
    }
    return '';
}

function stb_set_meta_description(int $post_id, string $value): void
{
    // Write to all known plugins so it sticks regardless of which one is active
    update_post_meta($post_id, '_yoast_wpseo_metadesc', $value);
    update_post_meta($post_id, 'rank_math_description', $value);
    update_post_meta($post_id, '_aioseo_description', $value);
}

function stb_record_revision(string $field, string $object, $old, $new): int
{
    $revs = get_option(STB_OPTION_REVISIONS, []);
    $rev_id = count($revs) + 1;
    $revs[] = [
        'rev_id' => $rev_id,
        'ts' => time(),
        'field' => $field,
        'object' => $object,
        'old' => $old,
        'new' => $new,
    ];
    // Cap at last 500 to keep options table small
    if (count($revs) > 500) {
        $revs = array_slice($revs, -500);
    }
    update_option(STB_OPTION_REVISIONS, $revs);
    return $rev_id;
}

function stb_rest_get_post_seo(WP_REST_Request $req): WP_REST_Response
{
    $id = (int)$req['id'];
    $post = get_post($id);
    if (!$post) {
        return new WP_REST_Response(['ok' => false, 'error' => 'Post not found'], 404);
    }
    return new WP_REST_Response([
        'ok' => true,
        'id' => $id,
        'title' => $post->post_title,
        'meta_description' => stb_get_meta_description($id),
        'permalink' => get_permalink($id),
        'status' => $post->post_status,
        'modified' => $post->post_modified,
    ]);
}

function stb_rest_update_post_seo(WP_REST_Request $req): WP_REST_Response
{
    $id = (int)$req['id'];
    $post = get_post($id);
    if (!$post) {
        return new WP_REST_Response(['ok' => false, 'error' => 'Post not found'], 404);
    }
    $body = $req->get_json_params() ?: [];
    $changes = [];

    if (isset($body['title'])) {
        $new = sanitize_text_field($body['title']);
        $old = $post->post_title;
        if ($new !== $old) {
            wp_update_post(['ID' => $id, 'post_title' => $new]);
            $rev_id = stb_record_revision('title', "post:$id", $old, $new);
            $changes[] = ['field' => 'title', 'rev_id' => $rev_id];
        }
    }

    if (isset($body['meta_description'])) {
        $new = sanitize_text_field($body['meta_description']);
        $old = stb_get_meta_description($id);
        if ($new !== $old) {
            stb_set_meta_description($id, $new);
            $rev_id = stb_record_revision('meta_description', "post:$id", $old, $new);
            $changes[] = ['field' => 'meta_description', 'rev_id' => $rev_id];
        }
    }

    return new WP_REST_Response(['ok' => true, 'changes' => $changes]);
}

function stb_rest_update_alt(WP_REST_Request $req): WP_REST_Response
{
    $id = (int)$req['id'];
    $post = get_post($id);
    if (!$post || $post->post_type !== 'attachment') {
        return new WP_REST_Response(['ok' => false, 'error' => 'Attachment not found'], 404);
    }
    $body = $req->get_json_params() ?: [];
    $new = isset($body['alt']) ? sanitize_text_field($body['alt']) : '';
    $old = (string)get_post_meta($id, '_wp_attachment_image_alt', true);
    if ($new !== $old) {
        update_post_meta($id, '_wp_attachment_image_alt', $new);
        $rev_id = stb_record_revision('alt', "attachment:$id", $old, $new);
        return new WP_REST_Response(['ok' => true, 'rev_id' => $rev_id]);
    }
    return new WP_REST_Response(['ok' => true, 'rev_id' => null, 'note' => 'no change']);
}

function stb_rest_set_schema(WP_REST_Request $req): WP_REST_Response
{
    $id = (int)$req['id'];
    $body = $req->get_json_params() ?: [];
    $jsonld = isset($body['jsonld']) ? wp_kses_post($body['jsonld']) : '';
    if (!$jsonld) {
        return new WP_REST_Response(['ok' => false, 'error' => 'jsonld required'], 400);
    }
    $old = (string)get_post_meta($id, '_stb_schema_jsonld', true);
    update_post_meta($id, '_stb_schema_jsonld', $jsonld);
    $rev_id = stb_record_revision('schema', "post:$id", $old, $jsonld);
    return new WP_REST_Response(['ok' => true, 'rev_id' => $rev_id]);
}

