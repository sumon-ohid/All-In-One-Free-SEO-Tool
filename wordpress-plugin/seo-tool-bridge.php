function stb_rest_list_posts(WP_REST_Request $req): WP_REST_Response
{
    $type = sanitize_key($req->get_param('type') ?? 'post');
    $limit = (int)($req->get_param('limit') ?? 50);
    $limit = max(1, min(200, $limit));

    $posts = get_posts([
        'post_type' => $type,
        'post_status' => 'publish',
        'numberposts' => $limit,
        'orderby' => 'modified',
        'order' => 'DESC',
    ]);

    return new WP_REST_Response([
        'ok' => true,
        'count' => count($posts),
        'posts' => array_map(function ($p) {
            return [
                'id' => $p->ID,
                'title' => $p->post_title,
                'permalink' => get_permalink($p->ID),
                'modified' => $p->post_modified,
                'meta_description' => stb_get_meta_description($p->ID),
            ];
        }, $posts),
    ]);
}

function stb_rest_revisions(): WP_REST_Response
{
    $revs = get_option(STB_OPTION_REVISIONS, []);
    return new WP_REST_Response([
        'ok' => true,
        'revisions' => array_slice(array_reverse($revs), 0, 100),
    ]);
}

function stb_rest_undo(WP_REST_Request $req): WP_REST_Response
{
    $rev_id = (int)$req['rev_id'];
    $revs = get_option(STB_OPTION_REVISIONS, []);
    $found = null;
    foreach ($revs as $r) {
        if (($r['rev_id'] ?? 0) === $rev_id) {
            $found = $r;
            break;
        }
    }
    if (!$found) {
        return new WP_REST_Response(['ok' => false, 'error' => 'Revision not found'], 404);
    }

    [, $object_id_str] = explode(':', $found['object']);
    $object_id = (int)$object_id_str;
    $field = $found['field'];
    $previous = $found['old'];

    switch ($field) {
        case 'title':
            wp_update_post(['ID' => $object_id, 'post_title' => (string)$previous]);
            break;
        case 'meta_description':
            stb_set_meta_description($object_id, (string)$previous);
            break;
        case 'alt':
            update_post_meta($object_id, '_wp_attachment_image_alt', (string)$previous);
            break;
        case 'schema':
            update_post_meta($object_id, '_stb_schema_jsonld', (string)$previous);
            break;
        default:
            return new WP_REST_Response(['ok' => false, 'error' => 'Unsupported field'], 400);
    }

    stb_record_revision($field, $found['object'], $found['new'], $previous);
    return new WP_REST_Response(['ok' => true, 'undone_rev_id' => $rev_id]);
}

/**
 * Resolve a public URL on this WP site to its post / page / CPT id.
 * Falls back through url_to_postid (core), the WP shortlink query, then
 * a manual home-relative path lookup. Returns 404 if nothing matches.
 */
function stb_rest_find_by_url(WP_REST_Request $req): WP_REST_Response
{
    $url = (string)$req->get_param('url');
    if (!$url) {
        return new WP_REST_Response(['error' => 'url required'], 400);
    }

    // Core resolver — handles permalinks, dates, taxonomies.
    $id = url_to_postid($url);
    if ($id > 0) {
        return new WP_REST_Response(['id' => $id]);
    }

    // Try the path relative to home_url (handles language-tagged etc.)
    $home = home_url('/');
    if (str_starts_with($url, $home)) {
        $path = substr($url, strlen($home));
        $maybe = url_to_postid(home_url('/' . ltrim($path, '/')));
        if ($maybe > 0) {
            return new WP_REST_Response(['id' => $maybe]);
        }
    }

    // Last resort: try matching by post_name (slug) of the trailing segment.
    $parts = parse_url($url);
    if (!empty($parts['path'])) {
        $segments = array_filter(explode('/', trim($parts['path'], '/')));
        $slug = end($segments);
        if ($slug) {
            $page = get_page_by_path($slug, OBJECT, ['post', 'page']);
            if ($page) {
                return new WP_REST_Response(['id' => $page->ID]);
            }
        }
    }

    return new WP_REST_Response(['error' => 'not found'], 404);
}

/**
 * Create a new WordPress post. Used by the SEO Tool's daily automation
 * when a blog_draft is approved + published.
 *
 * Body (JSON):
 *   - title           (string, required)
 *   - content         (string, required) — raw HTML
 *   - excerpt         (string, optional)
 *   - status          ("draft" | "publish", default "draft")
 *   - schemaJsonLd    (object | array | null) — saved as _stb_schema_jsonld
 *   - metaDescription (string, optional) — saved via stb_set_meta_description
 */
function stb_rest_create_post(WP_REST_Request $req): WP_REST_Response
{
    $body = $req->get_json_params();
    $title = isset($body['title']) ? sanitize_text_field((string)$body['title']) : '';
    $content = isset($body['content']) ? (string)$body['content'] : '';
    $excerpt = isset($body['excerpt']) ? sanitize_textarea_field((string)$body['excerpt']) : '';
    $status = isset($body['status']) && in_array($body['status'], ['draft', 'publish'], true)
        ? $body['status']
        : 'draft';

    if ($title === '' || $content === '') {
        return new WP_REST_Response(
            ['ok' => false, 'error' => 'title and content are required'],
            400,
        );
    }

    $post_id = wp_insert_post([
        'post_title'   => $title,
        'post_content' => wp_kses_post($content),
        'post_excerpt' => $excerpt,
        'post_status'  => $status,
        'post_type'    => 'post',
    ], true);

    if (is_wp_error($post_id)) {
        return new WP_REST_Response(
            ['ok' => false, 'error' => $post_id->get_error_message()],
            500,
        );
    }

    if (!empty($body['metaDescription'])) {
        stb_set_meta_description($post_id, (string)$body['metaDescription']);
    }
    if (!empty($body['schemaJsonLd'])) {
        update_post_meta(
            $post_id,
            '_stb_schema_jsonld',
            wp_json_encode($body['schemaJsonLd']),
        );
    }

    return new WP_REST_Response([
        'ok'  => true,
        'id'  => $post_id,
        'url' => get_permalink($post_id),
    ]);
}
